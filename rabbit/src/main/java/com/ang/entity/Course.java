public class Course {
    private int id;
    private String coname;
    private int cograde;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getConame() {
        return coname;
    }

    public void setConame(String coname) {
        this.coname = coname;
    }

    public int getCograde() {
        return cograde;
    }

    public void setCograde(int cograde) {
        this.cograde = cograde;
    }
}
